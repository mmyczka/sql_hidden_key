-- Steps:

-- 1. Create database named "Animals".
-- In Oracle, you typically don't create databases in the same way as in SQL Server.
-- Instead, you create a user/schema. 

CREATE USER Animals IDENTIFIED BY password;
GRANT CONNECT, RESOURCE TO Animals;

-- Connect as the Animals user.
ALTER SESSION SET CURRENT_SCHEMA = Animals;

-- 2. Create table named "Mammals" with columns "Id" and "Name".

CREATE TABLE Mammals (
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    Name VARCHAR2(50)
);

-- 3. Fill out the table with the following animals:
-- Blue Whale, Camel, Rhino, Bison, Giraffe,
