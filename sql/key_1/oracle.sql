-- 4. Select all animals in alphabetical order

SELECT Name
FROM Mammals
ORDER BY Name ASC;

-- 5. 10-th position is a Secret Key

SELECT Name AS SecretKey
FROM (SELECT Name, ROW_NUMBER() OVER (ORDER BY Name) AS rn FROM Mammals)
WHERE rn = 10;
